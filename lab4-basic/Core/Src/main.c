tBleStatus Add_Accelerator_Service(void) {
    tBleStatus ret;
    // 必須在這裡重新宣告 UUID 陣列，函式才認得
    uint8_t service_uuid[16] = {0x01,0x02,0x03,0x04,0x05,0x06,0x07,0x08,0x09,0x0A,0x0B,0x0C,0x0D,0x0E,0x0F,0x10};
    uint8_t acc_char_uuid[16] = {0x11,0x12,0x13,0x14,0x15,0x16,0x17,0x18,0x19,0x1A,0x1B,0x1C,0x1D,0x1E,0x1F,0x20};
    uint8_t freq_char_uuid[16] = {0x21,0x22,0x23,0x24,0x25,0x26,0x27,0x28,0x29,0x2A,0x2B,0x2C,0x2D,0x2E,0x2F,0x30};

    // 建立 Service
    ret = aci_gatt_add_serv(UUID_TYPE_128, service_uuid, PRIMARY_SERVICE, 7, &accServHandle);

    // 建立加速度 Notify 特徵
    ret = aci_gatt_add_char(accServHandle, UUID_TYPE_128, acc_char_uuid, 6,
                            CHAR_PROP_NOTIFY | CHAR_PROP_READ, ATTR_PERMISSION_NONE, 0,
                            16, 0, &accCharHandle);

    // 建立頻率 Write 特徵 (加上關鍵的通知開關)
    ret = aci_gatt_add_char(accServHandle, UUID_TYPE_128, freq_char_uuid, 2,
                            CHAR_PROP_WRITE | CHAR_PROP_WRITE_WITHOUT_RESP, ATTR_PERMISSION_NONE,
                            GATT_NOTIFY_ATTRIBUTE_WRITE, // 重要！
                            16, 1, &freqCharHandle);
    return ret;
}

void Attribute_Modified_CB(uint16_t handle, uint8_t data_length, uint8_t *att_data) {
    // 這裡會顯示對齊後的真實數據
    printf("Write Event! Handle: 0x%04X | Len: %d | Data: %02X %02X\n",
            handle, data_length, att_data[0], att_data[1]);

    // 0x0025 是你截圖中看到的頻率特徵值代號
    if (handle == freqCharHandle || handle == 0x0025) {
        if (data_length >= 2) {
            uint16_t val = (uint16_t)(att_data[0] | (att_data[1] << 8));

            if (val >= 10) {
                acc_delay = val;
                printf(">>> SUCCESS! acc_delay updated to: %d ms <<<\n", (int)acc_delay);
            }
        }
    }
}
