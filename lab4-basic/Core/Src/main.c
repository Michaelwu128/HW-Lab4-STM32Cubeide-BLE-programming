/**
* @brief Function implementing the TASK_ACC thread.
* @param argument: Not used
* @retval None
*
*/
extern uint16_t accServHandle;
extern uint16_t accCharHandle;
extern volatile uint32_t acc_delay;
/* USER CODE END Header_StartTaskACC */
void StartTaskACC(void const * argument)
{
  /* USER CODE BEGIN StartTaskACC */
  LSM6DSL_Object_t lsm6dsl_obj;
  LSM6DSL_Axes_t axes;
  int16_t pDataXYZ[3] = {0};

  // 設定 I2C 接口
  LSM6DSL_IO_t io_ctx;
  io_ctx.BusType  = LSM6DSL_I2C_BUS;
  io_ctx.Address  = 0xD4; // LSM6DSL_I2C_ADD_L (0x6A << 1)
  io_ctx.Init     = (void*)HAL_I2C_Init;
  io_ctx.DeInit   = (void*)HAL_I2C_DeInit;
  io_ctx.WriteReg = (void*)LSM6DSL_I2C_Write; // 使用我們寫的轉接頭
  io_ctx.ReadReg  = (void*)LSM6DSL_I2C_Read;  // 使用我們寫的轉接頭
  io_ctx.GetTick  = (void*)HAL_GetTick;

  // 初始化診斷
  printf("Initializing LSM6DSL...\n");
  if (LSM6DSL_RegisterBusIO(&lsm6dsl_obj, &io_ctx) != LSM6DSL_OK) {
      printf("Bus IO Registration Failed!\n");
  }

  if (LSM6DSL_Init(&lsm6dsl_obj) != LSM6DSL_OK) {
      printf("LSM6DSL Sensor Init Failed!\n");
  } else {
      printf("LSM6DSL Sensor Init Success!\n");
  }

  LSM6DSL_ACC_Enable(&lsm6dsl_obj);

  printf("--- Starting I2C Scanner ---\n");
  for (uint16_t i = 0; i < 255; i++) {
      if (HAL_I2C_IsDeviceReady(&hi2c2, i, 3, 5) == HAL_OK) {
          printf("Device found at address: 0x%02X\n", i);
      }
  }
  printf("--- Scan Finished ---\n");

  for(;;) {
    // 讀取加速度
    if (LSM6DSL_ACC_GetAxes(&lsm6dsl_obj, &axes) == LSM6DSL_OK) {
        pDataXYZ[0] = (int16_t)axes.x;
        pDataXYZ[1] = (int16_t)axes.y;
        pDataXYZ[2] = (int16_t)axes.z;
        // 只有讀到非零數據才印出來看
        if (pDataXYZ[0] != 0 || pDataXYZ[1] != 0) {
            printf("Acc: X=%d, Y=%d, Z=%d\n", pDataXYZ[0], pDataXYZ[1], pDataXYZ[2]);
        }
    }

    // 透過藍牙發送
    aci_gatt_update_char_value(accServHandle, accCharHandle, 0, 6, (uint8_t*)pDataXYZ);

    osDelay(acc_delay);
  }
  /* USER CODE END StartTaskACC */
}

